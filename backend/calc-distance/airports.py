def airport_coords(query: str):
    """Если в строке есть IATA-код аэропорта в скобках (REN) — вернёт его
    координаты (lat, lon, label). Иначе None."""
    import re
    m = re.search(r'\(([A-Z]{3})\)', query)
    if not m:
        return None
    code = m.group(1)
    coords = AIRPORTS.get(code)
    if not coords:
        return None
    return coords[0], coords[1], query
